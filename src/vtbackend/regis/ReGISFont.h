/// One glyph of the embedded ReGIS bitmap font: eight rows of eight pixels.
///
/// Each row byte carries eight columns; bit 0 (value 1) is the leftmost pixel, and @c rows[0] is the
/// top scanline. Glyphs are scaled to the requested text cell size when rendered.
struct Glyph
{
    std::array<uint8_t, 8> rows;
};

/// The width and height, in pixels, of a base glyph before scaling.
constexpr inline int GlyphBaseSize = 8;

/// The embedded bitmap font a ReGIS interpreter draws text with.
///
/// vtbackend must not depend on the font-shaping stack, and DEC ReGIS text was a hardware bitmap
/// font, so an embedded 8x8 ROM-style font is both layering-clean and faithful. The base table
/// covers printable ASCII (0x20..0x7E); @c L-command down-loadable alphabets are layered on top in a
/// later phase.
class ReGISBitmapFont
{
  public:
    /// @return the glyph for @p ch, or the blank glyph for characters outside the printable range.
    [[nodiscard]] Glyph const& glyphOf(char ch) const noexcept;

  private:
    // A down-loadable alphabet slot (the L command). Unused slots stay disengaged.
    // Populated in the extensibility phase; declared here so the interface is stable.
    std::array<std::optional<std::array<Glyph, 96>>, 4> _userAlphabets {};
};

